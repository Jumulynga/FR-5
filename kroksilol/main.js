function calc(operation, a, b){
    switch (operation, a, b) {
      case (operation === 'add') && a && b:
         console.log(a + b)
          break;
      case (operation === 'mult') && a && b:
          console.log(a * b)
          break;
      case (operation === 'sub') && a && b:
          console.log(a - b)
         break;
      default:
         break;
    }
  }

  calc('mult', 10, 15);
